package com.classonline.utils;

public class JobExtend {

	private int jobId;
	private String jobName;
	private int grade;
	private String pigaiTeacher;
	private String pigaiTime;
	private String url;
	public int getJobId() {
		return jobId;
	}
	public void setJobId(int jobId) {
		this.jobId = jobId;
	}
	public String getJobName() {
		return jobName;
	}
	public void setJobName(String jobName) {
		this.jobName = jobName;
	}
	public int getGrade() {
		return grade;
	}
	public void setGrade(int grade) {
		this.grade = grade;
	}
	public String getPigaiTeacher() {
		return pigaiTeacher;
	}
	public void setPigaiTeacher(String pigaiTeacher) {
		this.pigaiTeacher = pigaiTeacher;
	}
	public String getPigaiTime() {
		return pigaiTime;
	}
	public void setPigaiTime(String pigaiTime) {
		this.pigaiTime = pigaiTime;
	}
	public String getUrl() {
		return url;
	}
	public void setUrl(String url) {
		this.url = url;
	}
	
}
